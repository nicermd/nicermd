#!/usr/bin/env node
// Regenerate THIRD-PARTY-LICENSES.md from the production dependency
// trees of every distributable workspace package.
//
//   pnpm licenses:gen
//
// Re-run before each public release. Workspace packages (nicermd-core,
// nicermd-website) are not third-party and are excluded automatically
// by `pnpm licenses list`. Build-only tooling (vite, tsup, typescript,
// the Tauri CLI) is excluded by the --prod flag.
//
// The file is hand-checked into git so users browsing the repo see the
// attribution without needing to run anything.

import { execSync } from 'node:child_process'
import { readFileSync, readdirSync, writeFileSync } from 'node:fs'
import { join } from 'node:path'

const DISTRIBUTABLE = ['nicermd-website', 'nicermd-core']
const OUTPUT_PATH = 'THIRD-PARTY-LICENSES.md'
// Match LICENSE / LICENCE / COPYING / NOTICE with any common separator
// + suffix variant: LICENSE.md, LICENSE.txt, LICENSE-MIT.txt,
// LICENSE_APACHE-2.0, COPYING, etc.
const LICENSE_FILENAME = /^(LICEN[SC]E|COPYING|NOTICE)([-_.].+)?$/i

function listLicenses(filter) {
  const out = execSync(
    `pnpm licenses list --prod --filter ${filter} --json`,
    { encoding: 'utf-8', stdio: ['pipe', 'pipe', 'ignore'] },
  )
  return JSON.parse(out)
}

function findLicenseText(pkgPath) {
  try {
    const files = readdirSync(pkgPath)
    const matches = files.filter((f) => LICENSE_FILENAME.test(f)).sort()
    if (matches.length === 0) return null
    // Some packages ship multiple license files (e.g. dual-licensed
    // projects with one file per option). Concatenate with a header so
    // the source of each block is unambiguous.
    if (matches.length === 1) {
      return readFileSync(join(pkgPath, matches[0]), 'utf-8').trim()
    }
    return matches
      .map((f) => `===== ${f} =====\n\n${readFileSync(join(pkgPath, f), 'utf-8').trim()}`)
      .join('\n\n')
  } catch {
    return null
  }
}

function gather() {
  const all = new Map()
  for (const filter of DISTRIBUTABLE) {
    const data = listLicenses(filter)
    for (const [licenseGroup, entries] of Object.entries(data)) {
      for (const e of entries) {
        for (const version of e.versions) {
          const key = `${e.name}@${version}`
          if (all.has(key)) continue
          all.set(key, {
            name: e.name,
            version,
            license: e.license || licenseGroup,
            author: e.author || null,
            homepage: e.homepage || null,
            description: e.description || null,
            path: e.paths[0],
          })
        }
      }
    }
  }
  return [...all.values()].sort((a, b) => a.name.localeCompare(b.name))
}

function groupByLicense(packages) {
  const groups = new Map()
  for (const p of packages) {
    if (!groups.has(p.license)) groups.set(p.license, [])
    groups.get(p.license).push(p)
  }
  return [...groups.entries()].sort(([a], [b]) => a.localeCompare(b))
}

function render(packages) {
  const groups = groupByLicense(packages)
  const lines = []

  lines.push('# Third-party licenses')
  lines.push('')
  lines.push('Verbatim attribution for every package whose code is redistributed in')
  lines.push('the shipped Nicer.md artifacts (the website bundle deployed to')
  lines.push('nicermd.com, the Tauri desktop shell, and `nicermd-core` on npm).')
  lines.push('Build-only tooling (Vite, tsup, TypeScript, the Tauri CLI) is not')
  lines.push('redistributed and is excluded.')
  lines.push('')
  lines.push('Generated by `pnpm licenses:gen` from `pnpm licenses list --prod`.')
  lines.push('Re-run before each public release.')
  lines.push('')
  lines.push('## Summary')
  lines.push('')
  lines.push(`Total packages: **${packages.length}**`)
  lines.push('')
  for (const [license, pkgs] of groups) {
    lines.push(`- **${license}** — ${pkgs.length} package${pkgs.length === 1 ? '' : 's'}`)
  }
  lines.push('')
  lines.push('---')
  lines.push('')
  lines.push('## Packages')
  lines.push('')

  for (const [license, pkgs] of groups) {
    lines.push(`### ${license}`)
    lines.push('')
    for (const pkg of pkgs) {
      lines.push(`#### \`${pkg.name}@${pkg.version}\``)
      lines.push('')
      const meta = []
      if (pkg.author) meta.push(`Author: ${pkg.author}`)
      if (pkg.homepage) meta.push(`Homepage: <${pkg.homepage}>`)
      if (meta.length) {
        lines.push(meta.join('  \n'))
        lines.push('')
      }
      const text = findLicenseText(pkg.path)
      if (text) {
        lines.push('```')
        lines.push(text)
        lines.push('```')
      } else {
        lines.push(`*License text not bundled with the package; see homepage.*`)
      }
      lines.push('')
    }
  }

  return lines.join('\n')
}

const packages = gather()
const output = render(packages)
writeFileSync(OUTPUT_PATH, output)
console.log(`Wrote ${OUTPUT_PATH} (${packages.length} packages, ${output.length.toLocaleString()} bytes)`)
